class Materia():
    '''Representa un materia'''
    def __init__(self, nombreMateria, dias, inica, acaba):
        '''
        nombreMateria -> str
        dias -> tuple
        inia -> str
        acaba -> str
        '''
        self.nombreMateria = nombreMateria
        self.dias = dias
        self.inicia = inicia
        self.acaba = acaba


